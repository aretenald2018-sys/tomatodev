const parts = await Promise.all(STYLE_ENTRY_SOURCES.map(async (path) => {
  const source = await readFile(resolve(root, path), 'utf8');
  return `/* SOURCE: ${path} */\n${source.trimEnd()}\n`;
}));

const entry = [
  '/* GENERATED by scripts/generate-style-entry.mjs — do not edit directly. */',
  '/* A standalone bundle prevents partial rendering when a native WebView has a stale HTML cache. */',
  ...parts,
].join('\n');

await writeFile(resolve(root, 'style.css'), entry, 'utf8');
console.log(`[style-entry] bundled ${STYLE_ENTRY_SOURCES.length} sources`);
