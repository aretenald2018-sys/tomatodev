// DOM/Firebase 비의존 day/session -> 화면 상태 변환기.

export function workoutStateFromSession(source = {}, options = {}) {
  const sessionIndex = Math.max(0, Number(options.sessionIndex) || 0);
  return {
    sessionIndex,
    sessionId: source.id || `session-${sessionIndex + 1}`,
    exercises: Array.isArray(options.exercises) ? options.exercises : [],
    cf: !!source.cf,
    stretching: !!source.stretching,
    swimming: !!source.swimming,
    running: !!source.running,
    runData: {
      distance: source.runDistance || 0,
      durationMin: source.runDurationMin || 0,
      durationSec: source.runDurationSec || 0,
      memo: source.runMemo || '',
      source: source.runSource || 'manual',
      startedAt: source.runStartedAt || null,
      endedAt: source.runEndedAt || null,
      route: Array.isArray(source.runRoute) ? source.runRoute : [],
      routeRef: source.runRouteRef || null,
      routeSummary: source.runRouteSummary || null,
      placeSummary: source.runPlaceSummary || null,
      avgPaceSecPerKm: Number(source.runAvgPaceSecPerKm) || 0,
      gpsAccuracySummary: source.runGpsAccuracySummary || null,
    },
    cfData: {
      wod: source.cfWod || '',
      durationMin: source.cfDurationMin || 0,
      durationSec: source.cfDurationSec || 0,
      memo: source.cfMemo || '',
    },
    stretchData: {
      duration: source.stretchDuration || 0,
      memo: source.stretchMemo || '',
    },
    swimData: {
      distance: source.swimDistance || 0,
      durationMin: source.swimDurationMin || 0,
      durationSec: source.swimDurationSec || 0,
      stroke: source.swimStroke || '',
      memo: source.swimMemo || '',
    },
    wineFree: !!source.wine_free,
    workoutDuration: source.workoutDuration || 0,
    workoutTimeline: source.workoutTimeline || null,
    routineMeta: source.routineMeta || null,
    pickerGymFilter: source.pickerGymFilter || null,
    maxMeta: options.maxMeta || null,
    currentGymId: options.currentGymId || null,
  };
}

export function dietStateFromDay(day = {}) {
  return {
    breakfast: day.breakfast || '',
    lunch: day.lunch || '',
    dinner: day.dinner || '',
    snack: day.snack || '',
    breakfastSkipped: !!day.breakfast_skipped,
    lunchSkipped: !!day.lunch_skipped,
    dinnerSkipped: !!day.dinner_skipped,
    bOk: day.bOk ?? null,
    lOk: day.lOk ?? null,
    dOk: day.dOk ?? null,
    sOk: day.sOk ?? null,
    bKcal: day.bKcal || 0,
    lKcal: day.lKcal || 0,
    dKcal: day.dKcal || 0,
    sKcal: day.sKcal || 0,
    bReason: day.bReason || '',
    lReason: day.lReason || '',
    dReason: day.dReason || '',
    sReason: day.sReason || '',
    bProtein: day.bProtein || 0,
    bCarbs: day.bCarbs || 0,
    bFat: day.bFat || 0,
    lProtein: day.lProtein || 0,
    lCarbs: day.lCarbs || 0,
    lFat: day.lFat || 0,
    dProtein: day.dProtein || 0,
    dCarbs: day.dCarbs || 0,
    dFat: day.dFat || 0,
    sProtein: day.sProtein || 0,
    sCarbs: day.sCarbs || 0,
    sFat: day.sFat || 0,
    bFoods: Array.isArray(day.bFoods) ? day.bFoods : [],
    lFoods: Array.isArray(day.lFoods) ? day.lFoods : [],
    dFoods: Array.isArray(day.dFoods) ? day.dFoods : [],
    sFoods: Array.isArray(day.sFoods) ? day.sFoods : [],
    bEstimateMeta: day.bEstimateMeta || null,
    lEstimateMeta: day.lEstimateMeta || null,
    dEstimateMeta: day.dEstimateMeta || null,
    sEstimateMeta: day.sEstimateMeta || null,
  };
}
